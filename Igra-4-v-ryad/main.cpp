#include <iostream>
#include "Field.h"

using namespace std;

int main(void)
{
    setlocale(LC_ALL, ".UTF8");
    // создание объекта поля, первый ход за красным игроком (true)
    Field field(true);
    cout << "Игра '4 в ряд'" << endl;

    // основной игровой цикл: выполняется, пока игра не окончена (нет победителя и есть место)
    while (!field.isOver())
    {
        // текущее состояние игрового поля
        field.print();
        // чей сейчас ход
        cout << "Ходит "
            << (field.isRedTurnNow() ? "красный" : "желтый")
            << " игрок, введите ход (1-7)" << endl;

        int column;
        // попытка считать число из стандартного ввода
        if (!(cin >> column))
        {
            // если введено не число: сбрасываем состояние ошибки потока cin
            cin.clear();
            // возвращаемся в начало цикла для повторного ввода
            while (cin.get() != '\n');
            cout << "Некорректный ввод! Пожалуйста, введите число от 1 до 7." << endl;
            continue;
        }

        // попытка совершить ход в выбранную колонку
        if (!field.makeTurn(column))
        {
            cout << "Некорректный ход! Колонка заполнена или находится вне диапазона." << endl;
        }
    }

    // вывод финального состояния поля и объявление победителя (или ничьей)
    field.printResult();
    return 0;
}